year = int(input(" enter number of years: "));
day = 365;
week = 52;
month = 12;
hour = 8760;
days_in_year = year * day;
week_in_year = year * week;
month_in_year = year * month;
hours_in_year = year * hour;

print(f"in {year} years {days_in_year} days");
print(f"in {year} years {week_in_year} days");
print(f"in {year} years {month_in_year} days");
print(f"in {year} years {hours_in_year} days");
